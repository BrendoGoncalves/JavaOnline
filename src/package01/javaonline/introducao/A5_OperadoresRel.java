package package01.javaonline.introducao;

public class A5_OperadoresRel {
    public static void main(String[] args) {
        // < > <= >= == !=
        boolean isVar1 = 2 > 3;
        boolean isVar2 = 2 < 3;
        boolean isVar3 = 4 == 4;
        boolean isVar4 = 4 != 4;

        System.out.println(isVar1);
        System.out.println(isVar2);
        System.out.println(isVar3);
        System.out.println(isVar4);
    }
}
